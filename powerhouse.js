let currentIndex = 0;

const iconElement = document.getElementById("main-icon");
const titleElement = document.getElementById("title");
const descriptionElement = document.getElementById("description");
const servicesElement = document.getElementById("services");
const insidesElement = document.getElementById("insides");

function updateContent(index) {
    iconElement.src = content[index].icon;
    titleElement.innerHTML = content[index].title;
    descriptionElement.innerHTML = content[index].description;
    servicesElement.innerHTML = content[index].insideServices;
    insidesElement.innerHTML = content[index].insideContent;  // Update the "insides" section
}

document.getElementById("left-arrow").addEventListener("click", () => {
    currentIndex = (currentIndex > 0) ? currentIndex - 1 : content.length - 1;
    updateContent(currentIndex);
});

document.getElementById("right-arrow").addEventListener("click", () => {
    currentIndex = (currentIndex < content.length - 1) ? currentIndex + 1 : 0;
    updateContent(currentIndex);
});
